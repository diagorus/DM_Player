package com.dmplayer.models;

public class PlaylistItemInSeveral {
    private int id;
    private String name;

    public PlaylistItemInSeveral(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
